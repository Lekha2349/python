# if statement syntax
# The if statement in Python is used for conditional branching. It allows you to execute a block of code only if a certain condition is true. Here's the syntax of the if statement in Python:
# syntax
# if condition:
    # code block to execute if condition is true
# Indentation: The code block following the if statement must be indented. Python uses indentation to define blocks of code, so all statements indented at the same level are considered part of the same block.
x = 10
if x > 5:
    print("x is greater than 5")
# elif

if x > 10:
    print("x is greater than 10")
elif x == 10:
    print("x is equal to 10")
# else
if x > 10:
    print("x is greater than 10")
elif x == 10:
    print("x is equal to 10")
else:
    print("x is less than 10")

