# while
# In Python, the for loop is used to iterate over a sequence (such as a list, tuple, string, or range) and execute a block of code for each item in the sequence. Here's the syntax:

# for item in sequence:
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)
for i in range(5):
    print(i)
# while
# condition: This is a boolean expression. The loop continues to execute as long as this condition evaluates to True.
num = 0
while num < 5:
    print(num)
    num += 1
