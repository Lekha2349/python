# Arithmetic Operators
x = 10
y = 3
print("Arithmetic Operators:")
print("Addition:", x + y)
print("Subtraction:", x - y)
print("Multiplication:", x * y)
print("Division:", x / y)
print("Floor Division:", x // y)
print("Modulus:", x % y)
print("Exponentiation:", x ** y)
print()

# Comparison Operators
a = 5
b = 10
print("Comparison Operators:")
print("Equal to:", a == b)
print("Not equal to:", a != b)
print("Greater than:", a > b)
print("Less than:", a < b)
print("Greater than or equal to:", a >= b)
print("Less than or equal to:", a <= b)
print()

# Logical Operators
p = True
q = False
print("Logical Operators:")
print("Logical AND:", p and q)
print("Logical OR:", p or q)
print("Logical NOT:", not p)
print()

# Assignment Operators
c = 7
print("Assignment Operators:")
c += 3
print("Compound Assignment:", c)
print()

# Bitwise Operators
d = 5
e = 3
print("Bitwise Operators:")
print("Bitwise AND:", d & e)
print("Bitwise OR:", d | e)
print("Bitwise XOR:", d ^ e)
print("Bitwise NOT:", ~d)
print("Left shift:", d << 1)
print("Right shift:", d >> 1)
print()

# Identity Operators
f = [1, 2, 3]
g = [1, 2, 3]
print("Identity Operators:")
print("Is:", f is g)
print("Is not:", f is not g)
print()

# Membership Operators
h = [1, 2, 3]
print("Membership Operators:")
print("In:", 1 in h)
print("Not in:", 4 not in h)
print()

# String Concatenation Operator
str1 = "Hello"
str2 = " World"
print("String Concatenation Operator:")
print(str1 + str2)
